#include "Span.hpp"

//!-------------------------------CONSTRUCTOR-----------------------------------

Span::Span(unsigned int n):
	_size(n)
{

}

Span::Span(const Span &copy)
{
	*this = copy;
}

//!-------------------------------DESTRUCTOR------------------------------------

Span::~Span()
{

}

//!-------------------------------OPERATOR--------------------------------------

Span	&Span::operator=(const Span &copy)
{
	this->_size = copy.getSize();
	return(*this);
}

//!-------------------------------ACCESSORS-------------------------------------

unsigned int	Span::getSize() const
{
	return (this->_size);
}


